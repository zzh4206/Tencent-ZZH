"""
星火预习题目 — 2-5：狄拉克符号
=================================
用 Dirac 符号重写前面各小节的矩阵关系和定义式，
并写出 (|010⟩ - |101⟩)/√2 的列向量表示。

【Dirac 符号回顾】
|0⟩ = (1,0)^T,  |1⟩ = (0,1)^T
|ij⟩ = |i⟩⊗|j⟩  （张量积）
⟨ψ| = |ψ⟩†  （共轭转置）
⟨φ|ψ⟩ = 内积，|ψ⟩⟨φ| = 外积

【2-1 旋转矩阵】
R(θ) = cosθ·|0⟩⟨0| + sinθ·|0⟩⟨1| - sinθ·|1⟩⟨0| + cosθ·|1⟩⟨1|
v = |0⟩,  v' = R(θ)|0⟩ = cosθ|0⟩ - sinθ|1⟩

【2-2 矩阵指数】
泡利矩阵：
σ_x = |0⟩⟨1| + |1⟩⟨0|
σ_y = -i|0⟩⟨1| + i|1⟩⟨0|
σ_z = |0⟩⟨0| - |1⟩⟨1|
恒等算子：I = |0⟩⟨0| + |1⟩⟨1|

欧拉公式：e^{iθP} = cosθ·I + i sinθ·P  (当 P² = I)

【2-3 期望值】
⟨Q⟩ = ⟨ψ(θ)| Q |ψ(θ)⟩
其中 |ψ(θ)⟩ = e^{iθ/2 P} |0⟩

【2-4 张量积】
H = Σ_i Z_i + Σ_i X_i X_{i+1}
其中 Z_i = I⊗...⊗Z⊗...⊗I = I⊗...⊗(|0⟩⟨0|-|1⟩⟨1|)⊗...⊗I
⟨H⟩ = ⟨0...0| H |0...0⟩ = n
"""

import numpy as np

# ============================================================
# 计算 (|010⟩ - |101⟩)/√2
# ============================================================

# |0⟩ = (1,0)^T, |1⟩ = (0,1)^T
ket0 = np.array([1.0, 0.0])
ket1 = np.array([0.0, 1.0])

# |010⟩ = |0⟩⊗|1⟩⊗|0⟩
ket010 = np.kron(np.kron(ket0, ket1), ket0)
# |101⟩ = |1⟩⊗|0⟩⊗|1⟩
ket101 = np.kron(np.kron(ket1, ket0), ket1)

result = (ket010 - ket101) / np.sqrt(2)

print("=" * 60)
print("( |010⟩ - |101⟩ ) / √2  的列向量表示：")
print("=" * 60)
print(result)
print(f"\n维度: {len(result)} (2³ = 8)")
print(f"归一化检查: ||ψ||² = {np.sum(np.abs(result)**2):.6f}")

# 用二进制标注各分量
print("\n各基矢分量：")
for i in range(8):
    bits = f"{i:03b}"
    coef = result[i]
    if abs(coef) > 1e-10:
        print(f"  |{bits}⟩: {coef.real:+.6f}")

print("\n即: (|010⟩ - |101⟩)/√2 = (0, 0, 1/√2, 0, 0, 0, -1/√2, 0)^T")
print("对应 |010⟩ (第2位=1) 和 |101⟩ (第5位=1)，系数分别为 +1/√2 和 -1/√2。")

# 验证——重写 2-1 到 2-4
